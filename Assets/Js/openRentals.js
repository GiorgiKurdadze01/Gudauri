document.querySelector('.js-arrow-btn-1').addEventListener('click', function () {
    const boxContent = document.querySelector('.js-rentalBox-content-1');

    if (boxContent.style.display === 'none') {
        boxContent.style.display = 'block';
    } else {
        boxContent.style.display = 'none';
    }
});
